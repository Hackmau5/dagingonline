            <p class="mb-0 text-justify">The coronavirus pandemic has rocked food supply chains, with British beef and sheep farmers facing disruption caused by irrational buying in supermarkets and the loss of the food service market.

              But, for farmers who can sell directly to the consumer at farm shops and through online delivery, many have seen business increase, as shoppers turn to local produce in response to empty supermarket shelves and the most vulnerable being
              forced to self-isolate during the health crisis.

              This eagerness to buy British meat, produced locally, is proving a big boost for the Pasture-Fed Livestock Association (PFLA), a group of UK farmers passionate about raising animals purely on grass, while improving biodiversity and
              capturing carbon on their land.</p>

            <br><br>
            <p class="mb-0 text-justify">“It is an exciting experience to buy something direct and it is way more fun and rewarding than going to a supermarket,” says Jimmy Woodrow, head of consumer and supply chain at the PFLA.

              “I hope that is what will make people continue these new relationships they have built up.”

              Established in 2009, the association has grown to 585 members, 80% of whom are farmers with the rest being butchers and independent retailers. Its main aim is to increase the supply and market for 100% grass-fed meat.

              Mr Woodrow is confident that, despite the inevitable dip in sales once the pandemic subsides and supply chains steady, new customers will see the benefits of continuing to buy this produce locally.

              “I think there will be a new heightened ‘normal’ of sales in these channels,” he says.

              “There is no loyalty with the supermarket and people should be saying ‘we were together in the bad times, so we are together in the good times’ – not least when you taste what you are eating. It is a different product.”</p>

            <br><br>
            <p class="mb-0 text-left">
            <h1>Consumer awareness</h1><br>
            </p>
            <p class="mb-0 text-justify">With debates raging in the news and on social media about climate change and red meat consumption, the PFLA is helping its members market their meat, by educating consumers about the low-input methods used to
              produce it as well as highlighting the nutritional benefits.

              The association points to increasing scientific evidence, including work carried out at the University of Newcastle, that meat and milk from animals that have only ever eaten grass and pasture is different in nutritional quality – lower
              in saturated fat, a much healthier ratio of Omega-3 to Omega-6 fatty acids, and more vitamins and minerals.

              “The issue that we’ve always faced is how do we get more people to become aware of what we are doing, because generally when they do, it just makes sense,” Mr Woodrow says.

              “The role for me is about creating an environment and a structure within which enterprising farmers can succeed.”

              This includes plans to create an online livestock marketplace where certified PFLA farmers can trade animals, a project that has been accelerated because of the pandemic.

              “Farmers that have predominantly had restaurants as their sale outlet have struggled, so we are trying to divert those animals into other channels,” he adds.

              “You’ve got finishing farms who can’t access store cattle and then you’ve got suckler herds who don’t have an outlet for their store cattle, so we are trying to connect some of these dots.”</p>

            <br><br>
            <p class="mb-0 text-left">
            <h1>Climate research group</h1><br>
            </p>
            <p class="mb-0 text-justify">A research group of Pasture-Fed Livestock Association (PFLA) members has been formed to look at climate change, tasked with collecting data on the positive work farmers are doing around carbon sequestration
              and biodiversity.

              Peer-reviewed data is crucial in helping to prove that some livestock farmers are part of the solution to the climate crisis, not part of the problem, says Mr Woodrow.

              “We are looking to take more data on all our farms so that we can start proving we are potentially carbon neutral already,” he explains.

              “I want to get as many of our farms recording data so we can present something that is representative of the PFLA. But even on a single farm, if you have data to show your customers, I think that is also powerful.

              “We have a very motivated and energised membership who are looking to prove these points.”</p>

