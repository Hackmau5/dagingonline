            <p class="mb-0 text-justify">Is the coronavirus crisis pushing the U.S. toward a meat shortage?
              There’s not a shortage, exactly, say industry experts, though interruptions to the supply chain mean that it’s taking a little longer than usual for meat to get from a farm to your grocery store shelf.</p>

            <br><br>
            <p class="mb-0 text-justify">“We will have a short period where we have fewer packages of meat in the case,” said Daniel A. Sumner, director of the University of California Agricultural Issues Center. It will be an inconvenience, he said:
              “Let’s say you like thin-cut pork chops, I like thick-cut pork chops. Well, one of us will be disappointed if we shop late in the day.”

              The main problem is that more than 20 meat processing plants have had to close down temporarily because of COVID-19 outbreaks among workers, and those that remain open have had to slow down their typical operations because fewer
              employees can work than usual. On Tuesday, President Trump signed an executive order to keep meat processing plants open, classifying them as critical infrastructure, despite the objections of labor unions.</p>
            <br><br>
            <p class="mb-0 text-justify">“We are down 33-34% of total pork processing plant capacity,” said Jim Monroe, assistant vice president of communications for the National Pork Producers Council, due to plant closures and worker absenteeism.
              “The result of that, for hog farmers, is that hogs are backing up. There are too many hogs with nowhere to go.” That backup could lead to food waste and animal welfare concerns, he added.

              Some processing plants have had to close because of COVID-19 outbreaks. Hundreds of workers at Tyson Foods facilities across the nation have tested positive, and at least six have died, according to the Washington Post.</p>
            <br><br>
            <p class="mb-0 text-justify">One of the country’s largest meat producers, Tyson, took out full-page ads in the New York Times, Washington Post and Arkansas Democrat-Gazette on Sunday in which board chairman John Tyson warned that “the
              food supply chain is breaking.”
              “As pork, beef and chicken plants are being forced to close, even for short periods of time, millions of pounds of meat will disappear from the supply chain,” Tyson wrote. “As a result, there will be limited supply of our products
              available in grocery stores until we are able to reopen our facilities that are currently closed.”

              Monroe, of the National Pork Producers Council, agreed that “it’s a critical situation.” “Right now there isn’t a food shortage, but the longer this continues, the more you have to entertain that possibility,” he said.</p>
            <br><br>
            <p class="mb-0 text-justify">Another major meat producer, Smithfield Foods, closed meatpacking plants in three states after saying “a small number of employees” tested positive for the coronavirus, according to a company statement. (News
              reports put the number at one of the plants, in South Dakota, at 783.) The closure of plants as large as Smithfield’s, Sumner said, can result in dips to the meat supply that are noticeable to consumers.
            </p>
            <br><br>
            <p class="mb-0 text-justify">Meanwhile, at plants that remain open, workers are implementing physical distancing measures, which means meat is processed more slowly. Many workers who would have been standing close together on an assembly
              line now must stand farther apart, with plastic barriers between them, which means the line can’t move as fast as it normally would.

              And slower processing, which leads to shorter supply, inevitably results in higher prices at the grocery store. Paradoxically, however, livestock farmers are getting paid less because of the backup.

              “The prices for farmers have collapsed,” Sumner said. Just as dairy farmers have had to dump milk that they can't sell, some farmers have had to kill livestock without selling it to the market.

              The U.S. Department of Agriculture has stepped in, announcing that it will set up a “coordination center” to help meat producers who have closed their plants, including finding new markets for them. On April 19, as part of the $19
              billion federal Coronavirus Farm Assistance Program, the department said that it planned to purchase $3 billion in agricultural commodities, including $100 million per month in meat products.</p>
            <br><br>
            <p class="mb-0 text-justify">The idea is to buy some of the food that might have gone to restaurants and hotels, many of which have closed for now, and give it to food banks and nonprofits. That would help Americans who need food, and
              also make sure that producers get paid.

              A small silver lining, at least for Bay Area residents who can afford to spend a little extra on their ribeyes, is that the supply chain for higher-end, artisanal meat companies remains intact.

              “We pull from our own ranches, and so far we’ve been fine,” said Jim Offenbach, owner of Golden Gate Meat Co.
              His company mostly sells to restaurants, but he has seen an uptick in orders from grocery stores and in direct retail purchases.
              “Our shop at the Ferry Plaza has been on fire,” he said.
